     @Override
    public void run() {
        java.util.Calendar now = null;
        while(runClock == Thread.currentThread()){
            now = Calendar.getInstance();
            int hour = now.get(Calendar.HOUR);
            int min = now.get(Calendar.MINUTE);
            int sec = now.get(Calendar.SECOND);
            lblMin.setText(convert(hour));
            lblHour.setText(convert(min));
            lblSec.setText(convert(sec));
        }
    }   
    public void StartThread(){
        if(runClock == null){
            runClock = new Thread(this);
        }
        runClock.start();
    }
    public String convert(int values){
        return (values > 9) ?  Integer.toString(values): "0" + Integer.toString(values) ;
    }

