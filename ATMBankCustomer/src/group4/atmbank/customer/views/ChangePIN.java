    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        if(a==0){
            String txtcurrent=String.valueOf(txtCurrent.getPassword());
            checkcurrenpin =txtcurrent.equals(Account.getAccPIN());
            if(txtcurrent.equals("")){
                JOptionPane.showMessageDialog(null, "Please enter Current PIN");
            }else {
                if(checkcurrenpin==false){
                    JOptionPane.showMessageDialog(null, "Current PIN wrong,Please enter Current PIN again","Change PIN",JOptionPane.ERROR_MESSAGE);
                    txtCurrent.setText("");
                }
                else a=1;
            }
            
        }
        else if(a==1){
            btnChangePIN.setEnabled(true);
            btnEnter.setEnabled(false);
            a=2;
        }
    }//GEN-LAST:event_btnEnterActionPerformed

