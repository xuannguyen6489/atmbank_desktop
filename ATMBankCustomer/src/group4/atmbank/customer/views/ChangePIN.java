    private void btnChangePINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePINActionPerformed
        String txtnew=String.valueOf(txtNew.getPassword());
        String txtconfirm=String.valueOf(txtConfirm.getPassword());
        checknewpin=txtnew.equals(txtconfirm);
        if(checknewpin=false){
            JOptionPane.showMessageDialog(null, "Confirm New PIN wrong,Please Confirm New PIN again","Change PIN",JOptionPane.ERROR_MESSAGE);
            txtConfirm.setText("");
        }else{
            Account.setAccPIN(String.valueOf(txtNew.getPassword()));
            accountController.UpdateAccNo(Account);
            accountController.close();
            JOptionPane.showMessageDialog(null, "Change PIN successfully");
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnChangePINActionPerformed

