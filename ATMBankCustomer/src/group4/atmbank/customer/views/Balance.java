    public Balance(Control cl,Account account) {
        this.Account=account;
        ImageIcon icon = new ImageIcon(getClass().getResource("/images/logo.png"));
        Image image = icon.getImage();
        setIconImage(image);
        transactionController = new TransactionController();
        initComponents();
        
        int countWithdraw = transactionController.GetCountTransactionToDay(Account.getAccID(), "Withdraw");
        int countDeposit = transactionController.GetCountTransactionToDay(Account.getAccID(), "Deposit");
        double money=Account.getAccBalance();
        
        lblCardID.setText(Account.getAccNo());
        lblBalance.setText(ConvertFloatToString(money)+" $");
        lblDepositTimes.setText(String.valueOf(countDeposit));
        lblWithDrawTimes.setText(String.valueOf(countWithdraw));
        
        setLocationRelativeTo(null);
    }
    public static String ConvertFloatToString(double money){
        String moneys = String.valueOf(money);
        moneys = moneys.substring(0,moneys.lastIndexOf("."));
       return moneys;
   }
